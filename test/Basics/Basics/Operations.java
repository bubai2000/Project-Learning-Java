/*
 *  Sorted in order of precedence and associativity
 *  Unary ++,--(Unary postfix); ++,--,+,-,!(Unary Prefix) Associativity: Right to left
 *  Arithmetic /,*,%,+,- Associativity: Left to right
 *  Relational <,<=,>,>=,==,!= Associativity: Left to right
 *  Bitwise &,^,|,~ Associativity: Left to right
 *  Logical &&,|| Associativity: Left to right
 *  Ternary operator '?:'  Shorthand version of if-else statement. Associativity: Right to left
 *  Assignment =,+=,-=,*=,/=,%= Associativity: Right to left
 *
 *  Shift <<,>>,>>>
 *  Instance of operator: Used to test if an object is an instance of a class, a subclass or an interface.
 *
 *  If the operators have different precedence, solve the higher precedence first. If they have same precedence,
 *  solve according to associativity, that is either from right to left or from left to right.
 */
